package com.agendas.entidadesDTO;

public class AgendaDto {

	private Long cedulaMedico;
	private Long dia;
	private Long mes;
	private Long anio;
	
	
	//GETTERS AND SETTERS
	public Long getCedulaMedico() {
		return cedulaMedico;
	}
	public void setCedulaMedico(Long cedulaMedico) {
		this.cedulaMedico = cedulaMedico;
	}
	public Long getDia() {
		return dia;
	}
	public void setDia(Long dia) {
		this.dia = dia;
	}
	public Long getMes() {
		return mes;
	}
	public void setMes(Long mes) {
		this.mes = mes;
	}
	public Long getAnio() {
		return anio;
	}
	public void setAnio(Long anio) {
		this.anio = anio;
	}
	
	
}
